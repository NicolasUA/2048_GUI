package GUI.Additional;

import java.io.Serializable;

public class SimpleTile implements Serializable {
    private int x;
    private int y;
    private int number;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
